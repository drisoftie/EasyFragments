package com.drisoftie.frags;

import android.annotation.SuppressLint;
import android.app.Activity;
import android.app.Fragment;
import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

/**
 * A basic {@link Fragment} implementation with callbacks to setup and tear down components when UI is shown or teared down.
 *
 * @author Alexander Dridiger
 */
@SuppressLint("NewApi")
public abstract class FragManaged extends Fragment implements IFragManaged {

    @Override
    public void onAttach(Activity activity) {
        super.onAttach(activity);
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        return super.onCreateView(inflater, container, savedInstanceState);
    }

    @Override
    public void onActivityCreated(Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);
    }

    @Override
    public void onStart() {
        super.onStart();
    }

    @Override
    public final void onResume() {
        super.onResume();
        onResuming();
        if (getView() != null) {
            getView().post(new Runnable() {
                @Override
                public void run() {
                    if (FragManaged.this.getActivity() != null) {
                        registerComponents();
                    }
                }
            });
        }
    }

    @Override
    public final void onPause() {
        super.onPause();
        onPausing();
        deregisterComponents();
    }

    @Override
    public void onStop() {
        super.onStop();
    }

    @Override
    public void onDestroyView() {
        super.onDestroyView();
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
    }

    @Override
    public void onDetach() {
        super.onDetach();
    }

    @Override
    public View findViewById(int id) {
        return getView().findViewById(id);
    }
}
